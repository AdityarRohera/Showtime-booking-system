

-- SHOW SEATS TABLE

CREATE TABLE IF NOT EXISTS "SHOW_SEATS" (

    "SHOW_SEAT_ID" UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    "SHOW_ID" UUID NOT NULL,

    "SEAT_NO" VARCHAR(20) NOT NULL,

    "SEAT_TYPE" VARCHAR(50) NOT NULL,

    "STATUS" VARCHAR(20) NOT NULL DEFAULT 'AVAILABLE' CHECK (
        "STATUS" IN ('AVAILABLE', 'LOCKED', 'BOOKED', 'BLOCKED')
    ),

    "PRICE" NUMERIC(10,2) NOT NULL,

    "LOCKED_BY_USER_ID" UUID DEFAULT NULL,

    "LOCKED_AT" TIMESTAMP DEFAULT NULL,

    "LOCK_EXPIRY" TIMESTAMP DEFAULT NULL,

    "BOOKED_AT" TIMESTAMP DEFAULT NULL,

    "CREATED_AT" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    FOREIGN KEY ("SHOW_ID")
        REFERENCES "SHOWS"("SHOW_ID")
        ON DELETE CASCADE,

    FOREIGN KEY ("LOCKED_BY_USER_ID")
        REFERENCES "USERS"("UID")
        ON DELETE SET NULL,

    UNIQUE ("SHOW_ID", "SEAT_NO")

);