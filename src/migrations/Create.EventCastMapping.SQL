

-- EVENT ↔ CAST MAPPING TABLE

CREATE TABLE IF NOT EXISTS "EVENT_CASTS" (

    "EVENT_ID" UUID NOT NULL,

    "CAST_ID" UUID NOT NULL,

    PRIMARY KEY ("EVENT_ID", "CAST_ID"),

    FOREIGN KEY ("EVENT_ID")
        REFERENCES "EVENTS"("EVENT_ID")
        ON DELETE CASCADE,

    FOREIGN KEY ("CAST_ID")
        REFERENCES "CASTS"("CAST_ID")
        ON DELETE CASCADE

);