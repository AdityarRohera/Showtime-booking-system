

-- BOOKING SEATS TABLE

CREATE TABLE IF NOT EXISTS "BOOKING_SEATS" (

    "BOOKING_SEAT_ID" UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    "BOOKING_ID" UUID NOT NULL,

    "SHOW_SEAT_ID" UUID NOT NULL,

    "SEAT_NUMBER_SNAPSHOT" VARCHAR(20) NOT NULL,

    "SEAT_TYPE_SNAPSHOT" VARCHAR(50) NOT NULL,

    "PRICE_SNAPSHOT" NUMERIC(10,2) NOT NULL,

    "CREATED_AT" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    FOREIGN KEY ("BOOKING_ID")
        REFERENCES "BOOKINGS"("BOOKING_ID")
        ON DELETE CASCADE,

    FOREIGN KEY ("SHOW_SEAT_ID")
        REFERENCES "SHOW_SEATS"("SHOW_SEAT_ID")
        ON DELETE CASCADE

);